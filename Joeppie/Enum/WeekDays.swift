enum WeekDays: String {
    case EVERYDAY = "all_week"
    case MONDAY = "monday"
    case TUESDAY = "tuesday"
    case WEDNESDAY = "wednesday"
    case THURSDAY = "thursday"
    case FRIDAY = "friday"
    case SATURDAY = "saturday"
    case SUNDAY = "sunday"
    
    static let allValues = [EVERYDAY, MONDAY, TUESDAY, WEDNESDAY, THURSDAY, FRIDAY, SATURDAY, SUNDAY]
}
